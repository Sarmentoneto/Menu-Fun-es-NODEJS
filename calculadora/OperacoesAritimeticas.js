class OperacoesAritimeticas{
  static somar(num1,num2) { 
    this.num1 = num1,
    this.num2 = num2
    return num1 + num2}
   
  static subtrair(num1,num2) { 
     this.num1 = num1,
     this.num2 = num2
        return num1 - num2}
    
    static multiplicar(num1,num2) { 
     this.num1 = num1,
     this.num2 = num2
        return num1 * num2}

    static dividir(num1,num2) { 
     this.num1 = num1,
     this.num2 = num2
     return num1 / num2}
                

}

module.exports = OperacoesAritimeticas;